"""
flavor.py — Флавор-тексты для событий бота.
Все «ролевые» сообщения собраны здесь для удобного редактирования.
"""
import random

# ─── Банк: Пополнение счёта ─────────────────────────────────────────────────

DEPOSIT_FLAVORS = [
    "Ваш вклад зафиксирован. Мавродиус фон Батори лично благодарит вас за доверие.",
]

# ─── Банк: Снятие со счёта ──────────────────────────────────────────────────

WITHDRAW_FLAVORS = [
    "Ваш баланс уменьшился. Но не забывайте: истинное богатство — это вечность. А деньги мы ещё нарисуем!",
]

# ─── Кости: Критический успех ───────────────────────────────────────────────

CRIT_SUCCESS_FLAVORS = [
    "С таким везением тебе не по улицам Будапешта шляться, а в казино на острове Маргит раздевать лохов. Крит, господа!",
]

# ─── Кости: Критический провал ──────────────────────────────────────────────

CRIT_FAIL_FLAVORS = [
    "Ты хотел выглядеть пафосно, но в итоге просто эпично клацнул челюстью по пустоте. Весь Будапешт слышал этот позорный звук.",
]


def get_deposit_flavor() -> str:
    return random.choice(DEPOSIT_FLAVORS)


def get_withdraw_flavor() -> str:
    return random.choice(WITHDRAW_FLAVORS)


def get_crit_success_flavor() -> str:
    return random.choice(CRIT_SUCCESS_FLAVORS)


def get_crit_fail_flavor() -> str:
    return random.choice(CRIT_FAIL_FLAVORS)
